#include "libft.h"

char	*ft_strrchr(char *s, int c)
{
	size_t	i;
	char	*res;

	res = 0;
	i = 0;
	while (s[i])
	{
		if (s[i] == c)
		{
			res = (s + i);
		}
		i++;
	}
	if (c == '\0')
		return (s + i);
	return (res);
}
